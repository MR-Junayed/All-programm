x = int(input("value of X :"))
y = int(input("value of Y :"))
print("X=",x,"\n","Y=",y)
print("x == y: ", x == y)   # False
print("x != y: ",x != y)   # True
print("x > y: ",x > y)    # False
print("x < y: ",x < y)    # True
print("x >= y: ",x >= y)   # False
print("x <= y: ",x <= y)   # True